package simpleAlgorithm;

/**
 * 这个是冒泡排序，核心是数据的两两交换
 */
public class BubbleSort {

    public static void main(String[] args){
        int[] is = {2,5,8,5,3,0,5};

        for(int i=0;i<is.length;i++){
            for(int j=1;j<is.length-j;j++){
                int a,b,c;
                a=is[i];
                c=is[j];
            }
        }
    }
}
